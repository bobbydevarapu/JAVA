package JavaCoding.JavaBasic.BasicArrays;

public class twoDim {
    public static void main(String[] args) {
        int[][] matrix = {
            {1, 2, 3},
            {4, 5, 6},
            {7, 8, 9}
        };

        System.out.println();
        System.out.println("Element at row 1, column 2: " + matrix[0][1]); // Output: 2
    }
}