package JavaCoding.javaBasic.Statements;

public class ifLadder {
    public static void main(String[] args) {
        int num = 0;
        if (num > 0) {
            System.out.println("Positive number");
        } else if (num < 0) {
            System.out.println("Negative number");
        } else {
            System.out.println("Number is zero");
        }
    }
}
